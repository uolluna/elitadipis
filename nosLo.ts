/**
 * The radii of each corner of the rounded rectangle.
 */
interface CornerRadii {
    topLeft: number;
    topRight: number;
    bottomLeft: number;
    bottomRight: number;
}

// Usage example:
const radii: CornerRadii = {
    topLeft: 10,
    topRight: 15,
    bottomLeft: 5,
    bottomRight: 20
};
